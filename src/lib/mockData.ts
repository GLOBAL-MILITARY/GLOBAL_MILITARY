// CountryData interface is defined below

export interface CountryData {
    id: string;
    name: string;
    rank: number;
    powerIndex: number;
    flagUrl: string;
    mapUrl: string;
    coordinates: [number, number];
    modernizationLevel?: number; // 1-10 scale (1=0.5x, 10=2.0x)

    nuclear: {
        hasNuclear: boolean;
        stockpile: number;
        deployed: number;
    };

    navy: {
        totalShips: number;
        aircraftCarriers: number;
        submarines: number;
        destroyers: number;
        frigates: number;
        corvettes: number;
        patrolVessels: number;
        merchantMarine: number;
        majorPorts: number;
    };

    army: {
        activePersonnel: number;
        reservePersonnel: number;
        tanks: number;
        afv: number;
        selfPropelledArtillery: number;
        towedArtillery: number;
        rocketProjectors: number;
    };

    airforce: {
        totalAircraft: number;
        fighters: number;
        attackAircraft: number;
        transports: number;
        trainers: number;
        specialMission: number;
        tankerFleet: number;
        helicopters: number;
        attackHelicopters: number;
        bombers: number;
    };

    relations: {
        allies: Array<{ name: string; strength: number }>;
        potentialThreats: Array<{ name: string; strength: number }>;
    };
}

